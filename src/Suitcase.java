
public class Suitcase extends Bag
	{
	public Suitcase(double w, String s) 
		{
		super(w, s);		
		}
	
	public void weighs()
		{
		weight = weight;
		System.out.println("The suitcase weighs " + LuggageRunner.weight + " kgs.");
		}

	public void size()
		{
		System.out.println("The suitcase is " + size + ".");
		}
	}
